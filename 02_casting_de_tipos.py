print("casting de tipos de datos en python")

# para convertir un tipo de dato a otro se usa la funcion correspondiente

# int() para convertir a entero
print("conversion de enteros:")
print(int(10.5)) # esto es 10
print(int("10") + 2) # esto es 12

# para convertir a flotante
print("conversion de flotantes:")
print(float("3.1416")) # esto es 3.1416

# para convertir a cadena de texto
print("conversion de cadenas de texto:")
print(str(10)) # esto es "10"
print(str(100) + "20") # esto es "10020"

#para convertir a booleano
print("conversion de booleanos:")
print(bool(10)) # esto es True
print(bool(None)) # esto es False
print(bool("")) # esto es False
print(bool(" ")) # esto es True